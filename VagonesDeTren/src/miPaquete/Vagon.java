package miPaquete;

public class Vagon {
	private int id;
	private int agresividadTotal;

	public Vagon(int id, int agresividadTotal) {
		this.id = id;
		this.agresividadTotal = agresividadTotal;
	}

	public int getId() {
		return id;
	}

	public int getAgresividadTotal() {
		return agresividadTotal;
	}
	
	
}
